      TYPE(ONUMM3N2), INTENT(IN) :: X
      REAL(DP) :: DER0,DER1,DER2
      TYPE(ONUMM3N2) :: RES
      
      DER0 = ATAN(X%R)
      DER1 = 1D0/(X%R**2 + 1)
      DER2 = -2*X%R/(X%R**2 + 1)**2

      RES = FEVAL(X,DER0,DER1,DER2)

   END FUNCTION ONUMM3N2_ATAN

   ELEMENTAL FUNCTION ONUMM3N2_ACOS(X) RESULT(RES)

