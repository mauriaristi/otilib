   ELEMENTAL FUNCTION ONUMM3N2_POW_OR(X,E) RESULT(RES)

      TYPE(ONUMM3N2), INTENT(IN) :: X
      REAL(DP), INTENT(IN) :: E
      REAL(DP) :: DER0,DER1,DER2
      TYPE(ONUMM3N2) :: RES
      
      DER0=0.0d0
      DER1=0.0d0
      DER2=0.0d0
      
      DER0 = X%R**E
      IF ((E-0)/=0.0d0) THEN
         DER1 = E*X%R**(E - 1)
         IF ((E-1)/=0.0d0) THEN
            DER2 = E*X%R**(E - 2)*(E - 1)
         END IF
      END IF

      RES = FEVAL(X,DER0,DER1,DER2)

   END FUNCTION ONUMM3N2_POW_OR

   ELEMENTAL FUNCTION ONUMM3N2_POW_RO(E,X) RESULT(RES)

