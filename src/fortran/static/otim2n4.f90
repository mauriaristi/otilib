   ELEMENTAL FUNCTION ONUMM2N4_TAN(X) RESULT(RES)

      TYPE(ONUMM2N4), INTENT(IN) :: X
      REAL(DP) :: DER0,DER1,DER2,DER3,DER4
      TYPE(ONUMM2N4) :: RES
      
