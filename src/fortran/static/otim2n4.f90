  !!
  !***************************************************************************************************!
  PURE FUNCTION ONUMM2N4_det2x2(A) RESULT(det)

    IMPLICIT NONE

    TYPE(ONUMM2N4), INTENT(IN) :: A(2,2)   !! Matrix
    TYPE(ONUMM2N4)             :: det

    ! Calculate the determinant of the matrix
    det = (A(1,1)*A(2,2) - A(1,2)*A(2,1))

  END FUNCTION
