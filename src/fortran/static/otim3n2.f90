   ELEMENTAL FUNCTION ONUMM3N2_TAN(X) RESULT(RES)

      TYPE(ONUMM3N2), INTENT(IN) :: X
      REAL(DP) :: DER0,DER1,DER2
      TYPE(ONUMM3N2) :: RES
      
      DER0 = TAN(X%R)
      DER1 = TAN(X%R)**2 + 1
      DER2 = 2*(TAN(X%R)**2 + 1)*TAN(X%R)

      RES = FEVAL(X,DER0,DER1,DER2)

   END FUNCTION ONUMM3N2_TAN

   ELEMENTAL FUNCTION ONUMM3N2_COS(X) RESULT(RES)

