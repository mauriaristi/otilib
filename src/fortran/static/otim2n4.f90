      DER4 = 8*(TANH(X%R)**2 - 1)*(3*TANH(X%R)**2 - 2)*TANH(X%R)

      RES = FEVAL(X,DER0,DER1,DER2,DER3,DER4)

   END FUNCTION ONUMM2N4_TANH

